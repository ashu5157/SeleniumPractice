
public class SwitchCase1 {

	public static void main(String[] args) {
		int value=3;
		switch(value) {
		case 3:
			System.out.println("here is case 3 execute");
			break;
		case 4:
			System.out.println("here is case 4 execute");
			break;
		case 8:
			System.out.println("here is case 8 execute");
			break;
		default:
			System.out.println("sorry non of your case matched");
			break;
		}
	}

}
